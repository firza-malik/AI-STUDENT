/*22. Write a Java program to print a pattern of stars in the shape of a pyramid*/
public class Pattern {
    public static void main(String[] args) {
        int n=6;
         for(int i=0;i<n;i++){
            for(int j=0;j<n-i;j++){
                System.out.print(10);
         
            }
            System.out.println(" ");

         }

    }

    
}

